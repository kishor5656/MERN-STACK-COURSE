//switch statement 

let day ="wednesday"

switch (day) {
    case "monday":
        console.log("today is monday");
        break;
    case "tuesday":
        console.log("today is tuesday");
        break;
    case "wednesday":
        console.log("today is wednesday");
        break;
    case "thurdsay":
        console.log("today is thursday");
        break;
    default:
        console.log("enter a valid day")
}